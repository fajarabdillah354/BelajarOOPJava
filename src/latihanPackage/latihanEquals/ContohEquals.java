package src.latihanPackage.latihanEquals;

public class ContohEquals {


    public static void main(String[] args) {
        String first = "fajar";
        String call = "fajar";
        System.out.println(first == call);//tanda (==) hanya untuk di gunakan untuk mengecek data primitif
        System.out.println(first.equals(call));//tanda equals digunakan untuk data yang nonprimitif

        /*
        nilai yang akan dihasilkan adalah boolean
         */
    }


}
